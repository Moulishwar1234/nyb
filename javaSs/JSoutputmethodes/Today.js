// JS OUTPUT METHODES:-


// console.log():-
// Prints output to the browser developer console (used for debugging).
console.log("Hello, world!");
// Output: Hello, world!



// 2. console.error():-
// Prints an error message to the browser developer console.
console.error("Something went wrong");
// Output: Something went wrong


//  3. console.warn():-
// Prints a warning message to the browser developer console.
console.warn("This is a warning");

// Output: This is a warning


//  4. alert():-
// Displays an alert box with a specified message.
alert("Welcome to the site!");
// Output: Welcome to the site!



// 5. prompt():-
// Displays a prompt box that allows the user to input a value.
let name = prompt("What is your name?");
// Output: (user input)




// 6. confirm():-
// Displays a confirmation dialog box with a specified message.
let confirmResult = confirm("Are you sure?");
// Output: (true or false)




// 7. document.write():-
// Writes a string of text to a web page.
document.write("Hello, world!");
// Output: Hello, world!




// 8. innerHTML:-
// Sets or gets the HTML content of an element.
document.getElementById("demo").innerHTML = "Hello, world!";





//  9. console.table()
// Displays an array or object in a tabular format in the browser developer console.
 console.table([1, 2, 3, 4, 5]);
//  Output: A table with the array elements in rows and columns.





